#include "../../../includes/libft.h"

/*
 *	Makes {n} values of pointer {s} equal to zero
*/
void	ft_bzero(void *s, size_t n)
{
	unsigned char	*p;

	p = (unsigned char *)s;
	while (n--)
		*(p++) = 0;
}
